# Write a program to check if value 200 exists in the
# following dictionary:
# sample_dict = {'a':100,'b':200,'c':300,'d':400}

sample_dict = {'a':100,
               'b':200,
               'c':300,
               'd':400
}
print(sample_dict)
if "b" in sample_dict:
    print('200 exists in the following dictionary')
else:
    print('200 does not exists in the following dictionary')

